/** Convenience: get a single event by id */
export function getEvent(id: string): ETHDenverEvent {
  const e = sampleEvents.find((ev) => ev.id === id);
  if (!e) throw new Error(`Test fixture: no event with id "${id}"`);
  return e;
}

/** Get events filtered to a specific conference */
export function getConferenceEvents(conference: string): ETHDenverEvent[] {
  return sampleEvents.filter((e) => e.conference === conference);
}
